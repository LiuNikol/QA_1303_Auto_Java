package calculate;

public class calculate {
    public static int sum(int n, int m) {
        return n+m;
    }
    public static int sub(int n,int m) {
        return n-m;
    }
    public static int multiplication(int n, int m) {
        return n*m;
    }
    public static int division(int n, int m) {
        return n/m;
    }
    public static int percentage(int n, int m) {
        return n*100/m;
    }

}
